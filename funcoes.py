

def somar(num1, num2):
    resultado = num1 + num2
    return resultado

def sub(num1, num2):
    resultado = num1 - num2
    return resultado

def multiplicar(num1, num2):
    resultado = num1 * num2
    return resultado

def divide(num1, num2):
    if num2 == 0:
        return "Erro divisao"
    return num1 / num2


